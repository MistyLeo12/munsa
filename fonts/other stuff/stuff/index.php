	<p class="title">About the Conference</p>

	<p>Welcome to the official website of MUNSA XIV, the largest high school run Model United Nations conference 
	held in the western United States. MUNSA is hosted by <a href="http://www.neisd.net/isa" target="_blank">The International School of the Americas</a>, a public magnet
	school in the <a href="http://www.neisd.net" target="_blank">North East Independent School District</a>. On January 7th through January 9th, 2015, MUNSA will welcome 
	over 1200 delegates to the <a href="http://web.trinity.edu" target="_blank">Trinity University campus</a> in <a href="http://downtownsanantonio.org/" target="_blank">
	downtown San Antonio</a>.</p>
	
	<p>Topics of debate will range from "Transmission of Disease Across Borders" and "Discussing the Impact of Urbanization on Child Labor" to "Addressing the Lack of Maternal Health Care in Both Developed and Developing Countries" and "Analyzing Actions to Remove Landmines in East Africa" This year, MUNSA has assumed the theme of innovation and is implementing this theme into every committee room that will be hosted at the conference. This year's conference title, Visionary, is emblematic of this theme and of how delegates will be expected to act: working with the people of the world to solve problems through international cooperation and without compromising the authenticity of his or her country's position.	    </p>
	
	<p class="title2">Our Mission Statement</p>
	
	<p>Model United Nations San Antonio is a non-profit, student-run organization that seeks to create a dynamic learning opportunity for students from the San 
	Antonio area, the state of Texas, the United States of America, and Mexico. As a simulation, MUNSA strives to offer a forum in which academic, leadership, and interpersonal skills are combined to foster student discussion of our world's most pressing problems.
	
	</p> 
	

	


